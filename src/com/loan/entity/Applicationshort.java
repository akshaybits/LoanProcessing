package com.loan.entity;

public class Applicationshort {

	private Integer shortkey;
	private String shortfirstname;
	private String shortsubmissiondate;
	private String shortstatus;
	private String shortlastname;
	
	
	
	public String getShortsubmissiondate() {
		return shortsubmissiondate;
	}
	public void setShortsubmissiondate(String shortsubmissiondate) {
		this.shortsubmissiondate = shortsubmissiondate;
	}
	public String getShortstatus() {
		return shortstatus;
	}
	public void setShortstatus(String shortstatus) {
		this.shortstatus = shortstatus;
	}
	public Integer getShortkey() {
		return shortkey;
	}
	public void setShortkey(Integer shortkey) {
		this.shortkey = shortkey;
	}
	public String getShortfirstname() {
		return shortfirstname;
	}
	public void setShortfirstname(String shortfirstname) {
		this.shortfirstname = shortfirstname;
	}
	
	public String getShortlastname() {
		return shortlastname;
	}
	public void setShortlastname(String shortlastname) {
		this.shortlastname = shortlastname;
	}
	
	

}
